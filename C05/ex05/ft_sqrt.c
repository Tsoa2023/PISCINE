int	ft_sqrt(int nb)
{
	int	i;

	if (nb == 0)
		return (0);
	if (nb == 1)
		return (1);
	i = 0;
	while (nb > 0)
	{
		nb -= ((2 * i) + 1);
		i++;
	}
	if (nb == 0)
		return (i);
	return (0);
}
